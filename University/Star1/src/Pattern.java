public class Pattern {
    public static void main(String[] args){
        // pattern 4
        System.out.println("pattern 4");

        for(int i=0 ; i<5 ; i++){
            for (int j=0; j<5 ; j++){
                System.out.print("*" + " ");
            }
            System.out.println("");
        }
        System.out.println("");


        // pattern 5
//            *
//           **
//          ***
//         ****
//        *****

    }
}
