public class switchCase {
    public static void main(String[] args){

        int star =4;
        switch(star){
            case 1:
                System.out.println("rating is one");
                break;
            case 2:
                System.out.println("rating is two");
                break;
            case 3:
                System.out.println("rating is three");
                break;
            case 4:
                System.out.println("rating is four");
                break;
            case 5:
                System.out.println("rating is five");
                break;
                default:
                    System.out.println("ratings can only be b/w 1 to 5");
                    break;


        }

    }
}
